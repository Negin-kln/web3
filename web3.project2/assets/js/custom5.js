var favColor = prompt("enter your Favorite color");
switch (favColor) {
  case "blue":
    console.log("Your favorite color is blue.");
    break;
  case "red":
    console.log("Your favorite color is red.");
    break;
  case "green":
    console.log("Your favorite color is green.");
    break;
  case "purple":
    console.log("Your favorite color is purple.");
    break;
  default:
    console.log("Your favorite color was not found");
}
