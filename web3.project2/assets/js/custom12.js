var celsius = Number(prompt("enter a celsius"));
var fahrenheit = celsius * 1.8 + 32;
console.log(`fahrenheit = ${fahrenheit}`);
var celsius = (fahrenheit - 32) / 1.8;
console.log(`celsius = ${celsius}`);